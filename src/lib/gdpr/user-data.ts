const DIRECT_MESSAGE_ENCRYPTION_CONTEXT = 'changemappers:direct-message:v1';

function decryptDirectMessageRows<T extends { content?: string | null }>(messages: T[]): T[] {
  return messages.map((message) => {
    if (typeof message.content !== 'string') {
      return message;
    }
    return { ...message, content: decryptHighRiskContent(message.content, DIRECT_MESSAGE_ENCRYPTION_CONTEXT) };
  });
}

