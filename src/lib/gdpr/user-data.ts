type GdprDelegateMethod = (args: Record<string, unknown>) => Prisma.PrismaPromise<unknown>;
type GdprDelegate = Partial<Record<'findMany' | 'findUnique' | 'deleteMany' | 'updateMany', GdprDelegateMethod>>;

function getDelegate(client: GdprPrismaClient, model: string): GdprDelegate {
  return (client as unknown as Record<string, GdprDelegate>)[model] ?? {};
}

function callDelegate(
  client: GdprPrismaClient,
  model: string,
  method: 'findMany' | 'findUnique' | 'deleteMany' | 'updateMany',
  args: Record<string, unknown>,
): Prisma.PrismaPromise<unknown> {
  const delegate = getDelegate(client, model);
  const fn = delegate[method];
  if (!fn) throw new Error(`GDPR delegate missing: ${model}.${method}`);
  return fn(args);
}

function findMany(client: GdprPrismaClient, model: string, where: Record<string, unknown>) {
  return callDelegate(client, model, 'findMany', { where });
}

function deleteMany(client: GdprPrismaClient, model: string, where: Record<string, unknown>) {
  return callDelegate(client, model, 'deleteMany', { where });
}

function updateMany(client: GdprPrismaClient, model: string, where: Record<string, unknown>, data: Record<string, unknown>) {
  return callDelegate(client, model, 'updateMany', { where, data });
}

