export function getLanguageName(code: string, locale = 'hu'): string {
    try {
        const displayNames = new Intl.DisplayNames([locale], { type: 'language' });
        let name = displayNames.of(code);

        // If the browser lacks translation for the given locale and returns the code itself, fallback to English
        if (name?.toLowerCase() === code.toLowerCase() && locale !== 'en') {
            try {
                const enDisplayNames = new Intl.DisplayNames(['en'], { type: 'language' });
                const enName = enDisplayNames.of(code);
                if (enName && enName.toLowerCase() !== code.toLowerCase()) {
                    name = enName;
                }
            } catch {
                // ignore
            }
        }

        return name ? name.charAt(0).toUpperCase() + name.slice(1) : code;
    } catch {
        return code;
    }
}
